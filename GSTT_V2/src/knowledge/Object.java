package knowledge;



public class Object {

private String name;
private String color;
private String location;
private String size;
private String weight;
public Object(String name, String color,String location, String size, String weight){
	this.name=name;
	this.color=color;
	this.location=location;
	this.size=size;
	this.weight=weight;
}
public String getName() {
	
	return name;
}
public String getColor() {
	return color;
}
public String getLocation() {
	return location;
}
public String getSize() {
	return size;
}
public String getWeight() {
	return weight;
}
	
	
}
